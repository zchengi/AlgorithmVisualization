package six._07_Random_Maze_Generalization_with_Mist;

/**
 * 坐标
 *
 * @author cheng
 *         2018/3/19 13:50
 */
public class Position {

    private int x, y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
