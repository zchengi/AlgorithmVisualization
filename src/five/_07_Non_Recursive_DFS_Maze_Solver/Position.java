package five._07_Non_Recursive_DFS_Maze_Solver;

/**
 * 坐标
 *
 * @author cheng
 *         2018/3/13 19:57
 */
public class Position {

    private int x, y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
